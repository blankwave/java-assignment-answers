package Assignment3;
public class AllOperators {
    public static void main(String[] args) {
        System.out.println("Operators are used to perform operations on variables and values.");
        System.out.println("Operators are divided in following groups in JAVA:");
        System.out.println("-Arithmetic operators\n-Assignment operators\n-Comparison operators\n-Logical operators\n-Bitwise operators");
        System.out.println("\nArithmetic operators includes:");
        System.out.println("+ - * // % ++ --");
        System.out.println("\nAssignment operators includes:");
        System.out.println("= += -= *= /= %= &= |= ^= >>= <<=");
        System.out.println("\nComparison operators includes:");
        System.out.println("== != > < >= <=");
        System.out.println("\nLogical operators includes:");
        System.out.println("&& || !");
        System.out.println("\nBitwise operators includes:");
        System.out.println("& ^ ~ << >> >>>");
    }
}
